    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int row = myTasksTable.getSelectedRow();
    if (row < 0) {
        javax.swing.JOptionPane.showMessageDialog(null, "Please select a task to update!");
    } else {
        String id = myTasksTable.getValueAt(row, 0).toString();
        
       
        Object[] statuses = {"Pending", "In-Progress", "Completed"};
        String newStatus = (String) javax.swing.JOptionPane.showInputDialog(
                null, "Update Status for Task ID: " + id, "Status Update",
                javax.swing.JOptionPane.QUESTION_MESSAGE, null, statuses, statuses[0]);

        if (newStatus != null) {
           config conf = new config();
            String sql = "UPDATE tbl_tasks SET status = '" + newStatus + "' WHERE id = '" + id + "'";
            
            if (conf.insertData(sql) == 1) { 
                javax.swing.JOptionPane.showMessageDialog(null, "Status Updated!");
                displayMyTasks(); 
            }
        }
    }

    }//GEN-LAST:event_jButton1ActionPerformed

